package main

import (
	"fmt"
	"os/exec"
	"path/filepath"
	"strings"
	"time"

	configpkg "github.com/tuannvm/ccc/pkg/config"
	"github.com/tuannvm/ccc/pkg/telegram"
	"github.com/tuannvm/ccc/pkg/tmux"
)

// Worktree command handler.

// handleWorktreeCommand handles the /worktree command - create worktree session from existing session
func handleWorktreeCommand(config *Config, chatID, threadID int64, text string) {
	args := strings.TrimSpace(strings.TrimPrefix(text, "/worktree"))
	parts := strings.Fields(args)

	var baseSessionName, worktreeName string
	autoGenerated := false

	// Determine base session and worktree name based on args and context
	if len(parts) == 0 && threadID > 0 {
		// No args in topic: use current session as base, let Claude generate worktree name
		baseSessionName = getSessionByTopic(config, threadID)
		if baseSessionName == "" {
			telegram.SendMessage(config, chatID, threadID, "❌ No active session. Use /new to create a session first.")
			return
		}

		baseSession, exists := config.Sessions[baseSessionName]
		if !exists || baseSession == nil {
			telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("❌ Session '%s' not found.", baseSessionName))
			return
		}

		basePath := baseSession.Path
		if basePath == "" {
			basePath = configpkg.ResolveProjectPath(config, baseSessionName)
		}
		gitCmd := exec.Command("git", "-C", basePath, "rev-parse", "--is-inside-work-tree")
		if out, err := gitCmd.Output(); err != nil || strings.TrimSpace(string(out)) != "true" {
			telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("❌ Session '%s' is not a git repository: %s\n\nCannot create worktree. Initialize git first with 'git init'.", baseSessionName, basePath))
			return
		}

		providerName := baseSession.ProviderName
		if providerName == "" {
			providerName = config.ActiveProvider
		}

		existingWorktrees := getWorktreeNames(basePath)
		if existingWorktrees == nil {
			existingWorktrees = make(map[string]bool)
		}

		telegram.SendMessage(config, chatID, threadID, "🌳 Creating worktree...")
		listenLog("[/worktree] Starting Claude with --worktree for %s in %s (existing worktrees: %d)", baseSessionName, basePath, len(existingWorktrees))

		if err := tmux.SwitchSessionInWindow(baseSessionName, basePath, providerName, "", WorktreeAutoGenerate, false, false); err != nil {
			telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("❌ Failed to start Claude: %v", err))
			return
		}

		worktreeName = waitForNewWorktree(basePath, existingWorktrees, 30*time.Second)
		if worktreeName == "" {
			telegram.SendMessage(config, chatID, threadID, "❌ Failed to detect generated worktree name (timeout after 30s)\n\n💡 Tip: A worktree may have been created. Check with: git worktree list")
			return
		}

		listenLog("[/worktree] Detected worktree name: %s", worktreeName)
		worktreeSessionName := baseSessionName + "_" + worktreeName
		autoGenerated = true

		topicID, err := telegram.CreateForumTopic(config, worktreeSessionName, providerName, baseSessionName)
		if err != nil {
			telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("❌ Failed to create topic: %v", err))
			return
		}

		worktreePath := filepath.Join(basePath, ".claude", "worktrees", worktreeName)
		config.Sessions[worktreeSessionName] = &SessionInfo{
			TopicID:      topicID,
			Path:         worktreePath,
			ProviderName: providerName,
			IsWorktree:   true,
			WorktreeName: worktreeName,
			BaseSession:  baseSessionName,
		}
		configpkg.Save(config)

		if err := ensureHooksForSession(config, worktreeSessionName, config.Sessions[worktreeSessionName]); err != nil {
			listenLog("[/worktree] Failed to install hooks for %s: %v", worktreeSessionName, err)
		}

		if err := tmux.SwitchSessionInWindow(worktreeSessionName, basePath, providerName, "", worktreeName, false, false); err != nil {
			telegram.SendMessage(config, config.GroupID, topicID, fmt.Sprintf("❌ Failed to start session: %v", err))
			return
		}

		providerMsg := ""
		if providerName != "" {
			providerMsg = fmt.Sprintf("\n🤖 Provider: %s", providerName)
		}
		worktreeMsg := fmt.Sprintf("🌳 Worktree session '%s' started!\nBase: %s\nWorktree: %s%s\n\n✨ Worktree name was auto-generated by Claude", worktreeSessionName, baseSessionName, worktreeName, providerMsg)
		worktreeMsg += "\n\nSend messages here to interact with Claude."
		telegram.SendMessage(config, config.GroupID, topicID, worktreeMsg)
		return
	}

	if len(parts) == 1 && threadID > 0 {
		worktreeName = parts[0]
		baseSessionName = getSessionByTopic(config, threadID)
		if baseSessionName == "" {
			telegram.SendMessage(config, chatID, threadID, "❌ No active session. Use /new to create a session first, or use: /worktree <session_name> <worktree_name>")
			return
		}
	} else if len(parts) == 1 {
		telegram.SendMessage(config, chatID, threadID, "Usage: /worktree <session_name> <worktree_name>\n\nCreates a new worktree session from an existing session's repository.\n\nTip: In a session topic, you can use just /worktree <name>.")
		return
	} else {
		baseSessionName = parts[0]
		worktreeName = parts[1]
	}

	baseSession, exists := config.Sessions[baseSessionName]
	if !exists || baseSession == nil {
		telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("❌ Base session '%s' not found. Use /new to create it first.", baseSessionName))
		return
	}

	basePath := baseSession.Path
	if basePath == "" {
		basePath = configpkg.ResolveProjectPath(config, baseSessionName)
	}
	gitCmd := exec.Command("git", "-C", basePath, "rev-parse", "--is-inside-work-tree")
	if out, err := gitCmd.Output(); err != nil || strings.TrimSpace(string(out)) != "true" {
		telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("❌ Base session path is not a git repository: %s", basePath))
		return
	}

	worktreeSessionName := baseSessionName + "_" + worktreeName

	if _, exists := config.Sessions[worktreeSessionName]; exists {
		telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("⚠️ Worktree session '%s' already exists. Use /new in that topic to restart.", worktreeSessionName))
		return
	}

	providerName := baseSession.ProviderName
	if providerName == "" {
		providerName = config.ActiveProvider
	}

	topicID, err := telegram.CreateForumTopic(config, worktreeSessionName, providerName, baseSessionName)
	if err != nil {
		telegram.SendMessage(config, chatID, threadID, fmt.Sprintf("❌ Failed to create topic: %v", err))
		return
	}

	worktreePath := filepath.Join(basePath, ".claude", "worktrees", worktreeName)
	config.Sessions[worktreeSessionName] = &SessionInfo{
		TopicID:      topicID,
		Path:         worktreePath,
		ProviderName: providerName,
		IsWorktree:   true,
		WorktreeName: worktreeName,
		BaseSession:  baseSessionName,
	}
	configpkg.Save(config)

	if err := ensureHooksForSession(config, worktreeSessionName, config.Sessions[worktreeSessionName]); err != nil {
		listenLog("[/worktree] Failed to install hooks for %s: %v", worktreeSessionName, err)
	}

	if err := tmux.SwitchSessionInWindow(worktreeSessionName, basePath, providerName, "", worktreeName, false, false); err != nil {
		telegram.SendMessage(config, config.GroupID, topicID, fmt.Sprintf("❌ Failed to start session: %v", err))
		return
	}

	providerMsg := ""
	if providerName != "" {
		providerMsg = fmt.Sprintf("\n🤖 Provider: %s", providerName)
	}
	worktreeMsg := fmt.Sprintf("🌳 Worktree session '%s' started!\nBase: %s\nWorktree: %s%s", worktreeSessionName, baseSessionName, worktreeName, providerMsg)
	if autoGenerated {
		worktreeMsg += "\n\n✨ Worktree name was auto-generated"
	}
	worktreeMsg += "\n\nSend messages here to interact with Claude."
	telegram.SendMessage(config, config.GroupID, topicID, worktreeMsg)
}
